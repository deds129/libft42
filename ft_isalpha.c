#include "libft.h"

int	ft_isalpha(int c)
{
	if (c >= 'A' && c <= 'Z')
	{
		return (c - '0');
	}
	if (c >= 'a' && c <= 'z')
	{
		return (c - '0');
	}
	return (0);
}
